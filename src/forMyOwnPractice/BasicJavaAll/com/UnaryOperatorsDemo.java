package forMyOwnPractice.BasicJavaAll.com;

public class UnaryOperatorsDemo {
    public static void main(String[] args) {

        int x =10;

        x++;
        System.out.println(" Using ++ :"+ x);

        x--;
        System.out.println(" Using ++ :"+ x);

        ++x;
        System.out.println(" Using ++ :"+ x);

        --x;
        System.out.println(" Using ++ :"+ x);
    }
}
